# Python code to demonstrate the working of log2() and log10()
# Import the 'math' module to do the mathematical calculations
from math import log2, log10
number = int(input("Enter a number : "))

# Working of log2() function
print(f'Working of log2() when number ({number}) is : {log2(number)}')

# Working of log10() function
print(f'Working of log10() when number ({number}) is : {log10(number)}')