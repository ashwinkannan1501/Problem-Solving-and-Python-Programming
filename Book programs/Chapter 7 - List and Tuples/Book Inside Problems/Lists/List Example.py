# List Example
"""A list is a collection of ordered element of same (or) different data type. It is represented
as "[]". List is a mutable (i.e) changeable. We can able to change the list after instantiation.
SYNTAX : variable_name = []"""

# Creating a list with integer data type
int_list_element = [1, 6, 2, 8, 5, 6, 4]

# Creating an empty lists
empty_list = []

# Creating a lists with multiple data type
multiple_data_type_lists = [1, 20.6, "ashwin"]

# Creating a list with string data type
string_list_elements = ["ashwin", "kannan", "amutha"]

# Creating a list with float data type
float_data_type = [12.3, 15.3, 45.3, 12.6]

# Creating a list within the another list
nested_list = [[1, 0], [2, 2]]

print(f'Integer list elements : {int_list_element}')
print(f'Float list elements : {float_data_type}')
print(f'String list elements : {string_list_elements}')
print(f'Nested list elements : {nested_list}')
print(f'Multiple data type list : {multiple_data_type_lists}')
print(f'Empty lists : {empty_list}')
