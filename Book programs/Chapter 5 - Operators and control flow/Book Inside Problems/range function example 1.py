# Python program using range with one parameter
number = int(input("Enter a random number : "))
print('The numbers in the for loop are : ')
for random in range(1, number+1):
    print(random)

# Print out 3, 4, 5 with two parameter
print("The numbers in range of 3 to 5 is : ")
for random in range(3, 6):
    print(random)
