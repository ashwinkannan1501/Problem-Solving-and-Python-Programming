# VARIABLES
# Assigning values to the variables

# An integer assignment
integer_variable = int(input("Enter an integer : "))
print(f'An integer assignment variable = {integer_variable}')

# An floating point assignment
floating_point_variable = float(input("Enter a floating point : "))
print(f'An floating point assignment variable = {floating_point_variable}')

# An string assignment
string_variable = input("Enter the string : ")
print(f'An string assignment variable = {string_variable}')

# Multiple assignments
# Assigning an integer value to multiple variables (first, second, third)
first = second = third = int(input("Enter an integer : "))
print(f'First number = {first} \nsecond number = {second} \nthird number = {third}')

# Assigning multiple values to the multiple variable in the same line
fourth, fifth, sixth = 10, 20, 30
print(f'fourth number = {fourth} \nfifth number = {fifth} \nsixth number = {sixth}')
