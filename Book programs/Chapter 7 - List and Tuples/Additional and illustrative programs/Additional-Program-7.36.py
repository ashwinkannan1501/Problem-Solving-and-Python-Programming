# Python program to convert a tuple into a dictionary
tuple_elements = ((1, 2), (3, 4))
print(f'The tuple elements are : {tuple_elements}')
print(f'The data type is : {type(tuple_elements)}')
print(f'The dictionary elements is : {dict(tuple_elements)}')
print(f'The data type is : {type(dict(tuple_elements))}')
