# Python program to get the volume of sphere

pi = 3.14  # Define pi as 3.14

radius = int(input("Enter the radius value : "))  # Get the radius value from the user

volume_of_sphere = (4/3 * pi * radius ** 3)  # Calculate the "volume of sphere" by using it's formula

print(f"The volume of sphere of radius '{radius}' is : {volume_of_sphere}")  # Print the result
