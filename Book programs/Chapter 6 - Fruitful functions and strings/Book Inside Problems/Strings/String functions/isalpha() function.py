# isalpha() function - (isalphabet function)
"""The isalpha() function returns True if all the characters in the strings are alphabets otherwise
it returns False. It doesn't take any parameters
SYNTAX : string.isalpha()"""

# Python program to check if all the characters in the strings are alphabetic characters
string = input("Enter the string : ")
print(f'The string ({string}) is alphabetic characters : {string.isalpha()}')
