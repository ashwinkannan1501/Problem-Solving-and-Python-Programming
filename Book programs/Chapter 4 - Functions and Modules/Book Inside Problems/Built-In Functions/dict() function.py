# Python dict()
"""The dict() function creates a dictionary in python
SYNTAX : dict(**Kwargs)
         dict(mapping, **Kwargs)
         dict(iterable, **Kwargs)"""

dictionary = dict(x=5, y=0)
print(f'dictionary numbers = {dictionary}')
print(f'Data Type : {type(dictionary)}')

empty_dictionary = {}
print(f'Empty dictionary = {empty_dictionary}')
print(f'Data type : {type(empty_dictionary)}')
