# Python program to display your details like name, age, address in 3 different lines

# Getting the 'name', 'age', 'address' details from the user
name = input("Enter your name : ")  # Get the name from the user

age = int(input("Enter your age : "))  # Get the age from the user

address = input("Enter your house address : ")  # Get the house address from the user

# Printing the user's 'name', 'age', 'address'
print("Name : ", name)  # Print the user's name

print("Age : ", age)  # Print the user's age

print("Address : ", address)  # Print the user's address
