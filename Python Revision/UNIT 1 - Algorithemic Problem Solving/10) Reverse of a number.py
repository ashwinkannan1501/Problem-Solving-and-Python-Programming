# Reverse of a number

number = int(input("Enter a number : ")) # Get a integer value from the user namely "number".

string = str(number) # Convert a integer value into a string value

reverse_number = string[::-1] # Reverse a number by setting only the index value as "-1"

print(f"The reverse of {number} is : {reverse_number}") # Display the "reverse_number" value




    
