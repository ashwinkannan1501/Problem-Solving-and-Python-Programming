# Area and Circumference of the circle

pi = 3.14  # Assign the value of pi as 3.14

radius = int(input("Enter the radius value : "))  # Get the integer value from the user and store it in a variable "radius"

area_of_circle = pi * radius * radius  # Calculate the "area of circle" by applying the formula
circumference_of_circle = 2 * pi * radius # Calculate the "circumference of circle" by applying the formula

print(f"Area of circle of radius {radius} is : {area_of_circle}") # Display the "area of circle" value
print(f"Circumference of circle of radius {radius} is : {circumference_of_circle}") # Display "circumference of circle" value
