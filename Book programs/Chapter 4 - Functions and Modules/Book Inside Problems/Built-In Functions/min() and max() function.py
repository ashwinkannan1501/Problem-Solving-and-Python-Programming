# Python min() and max() function
"""min() function - The min() function returns the smallest element in an group of elements
SYNTAX : min(object)"""
"""max() function - The max() function returns the largest element in a group of elements
SYNTAX : max(object)"""
list_elements = [6, 10, 50, 1, 0, 63, 36, 925, 25, 654, 98, 100]
# list_elements = input("Enter the list elements : ")
# list_elements = list(list_elements.split(","))
print(f'The minimum element in the {list_elements} is : {min(list_elements)}')
print(f'The maximum element in the {list_elements} is : {max(list_elements)}')
