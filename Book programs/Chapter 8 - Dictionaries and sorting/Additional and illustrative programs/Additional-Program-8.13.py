# Python program to get the maximum and minimum value in a dictionary
dictionary = {1: 2, 3: 4, 5: 6}

# Maximum and minimum keys in the dictionary
print(f'Minimum key in a dictionary : {min(dictionary.keys())}')
print(f'Maximum key in a dictionary : {max(dictionary.keys())}')
# Maximum and minimum values in the dictionary
print(f'Minimum value in a dictionary : {min(dictionary.values())}')
print(f'Maximum value in a dictionary : {max(dictionary.values())}')

# Maximum and minimum items in the dictionary
print(f'Minimum items in a dictionary : {min(dictionary.items())}')
print(f'Maximum items in a dictionary : {max(dictionary.items())}')
