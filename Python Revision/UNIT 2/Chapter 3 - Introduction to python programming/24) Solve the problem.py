# Python program to solve (x + y) * (x + y)

x = int(input("Enter the x value : "))  # Get the x value from the user
y = int(input("Enter the y value : "))  # Get the y value from the user

print("The solution of (x + y) * (x + y) is : ", (x + y) ** 2)  # Compute the problem and print the result
