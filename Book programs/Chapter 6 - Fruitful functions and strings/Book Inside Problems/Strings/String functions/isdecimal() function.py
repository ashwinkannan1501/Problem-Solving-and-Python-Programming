# isdecimal() function
"""The isdecimal() function returns True if all the characters in a string are decimal numbers
otherwise it returns False. It doesn't take any parameters
SYNTAX : string.isdecimal()"""

# Python program to check if all the characters in the strings are decimal numbers
string = input("Enter a string : ")
print(f'The string ({string}) is a decimal numbers : {string.isdecimal()}')
