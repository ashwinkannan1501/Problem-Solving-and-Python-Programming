# Odd or Even

number = int(input("Enter a number : ")) # Get the input from the user namely "number"

if((number % 2) == 0): # Check the condition if (number%2==0) or (number%2!=0) to find the number is even or odd
    print(f"The number {number} is a even number")
else :
    print(f"the number {number} is a odd number")
