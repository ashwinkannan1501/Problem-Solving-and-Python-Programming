# sort() function
"""The sort() function is sed to sorts the unordered list to ordered list
SYNTAX : list.sort()"""

list_elements = [100, 56, 65, 2, 0, 36, 55, 41, 200, 8, 5, 1]

# Before sorting
print(f'Before sorting : {list_elements}')

# After sorting
list_elements.sort()
print(f'After sorting : {list_elements}')
