# Python program to unpack a tuple elements in several variables
tuple_elements = (1, 2, 3, 4, 5, 6)
print(f"The tuple elements are : {tuple_elements}")
p, q, r, s, t, w = tuple_elements # assigning a single value into mutiple variable
print(f'The addition of tuple elements are : {p+q+r+s+t+w}')
