# title() function
"""The title() function returns a string with the first letter of each word capitalized as a title
cased string. This function doesn't take any parameters
SYNTAX : string.title()"""
# Example program to make the first letter of each word capitalized in a string
string = input("Enter a string : ")
print(f'Before using title() function : {string}')
print(f'After using title() function : {string.title()}')
