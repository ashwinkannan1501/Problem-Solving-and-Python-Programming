# Area of a circle

pi = 3.14  # Assign pi as 3.14

radius = int(input("Enter the radius value : "))  # Get the radius value from the user

area_of_circle = pi * radius ** 2  # Calculate the area of circle by using the formula

print(f"The area of circle of radius '{radius}' is : {area_of_circle}")  # Print the result
