# Positive or negative

number = int(input("Enter a number : ")) # Get a integer value from the user namely "number"

if(number > 0): # Check the condition if the number is > 0 or < 0 to find the positive or negative number
    print(f"The number {number} is a positive number")
else :
    print(f"The number {number} is a negative number")
    
