# Program to calculate the average of numbers in a list

list_elements = [10, 20, 50, 13, 45, 65, 85, 63]

sum_of_list_elements = sum(list_elements)
print(f'The sum of list elements ({list_elements} is : {sum_of_list_elements}')

average_list_element = sum_of_list_elements / len(list_elements)
print(f'The average of list elements ({list_elements}) is : {average_list_element}')
