# isalnum() function - (isalphanumeric function)
"""The isalnum() function returns True if all the characters in the string are alphanumeric
characters otherwise it returns False. The isalnum() function doesn't take any parameters
SYNTAX : string.isalnum()"""

# Python program to check if all the characters in the strings are alphanumeric characters
string = input("Enter the string : ")
print(f'The string ({string}) is a alphanumeric characters : {string.isalnum()}')
