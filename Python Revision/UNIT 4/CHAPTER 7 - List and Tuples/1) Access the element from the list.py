# Python program to access elements from the list

lists = [85, 1.36, 9j, True, "String", ["Ashwin", "Kannan", "Amutha"], ('Vignesh', 'Varatharaj', 'JP'),
         {'Reshma', 'Kirthika', 'Supiya'}, {'Ashwin':'Reshma', 'Kannan': 'Amutha'}]

print("lists[0] = ", lists[0])
print("lists[-1] = ", lists[-1])
print("lists[5] = ", lists[5])

print("lists[5][0] = ", lists[5][0])
print('lists[6][0] = ', lists[6][0])
print("lists[8]['Ashwin'] = ", lists[8]['Ashwin'])
