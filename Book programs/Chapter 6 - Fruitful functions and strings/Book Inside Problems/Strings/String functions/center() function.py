# center() function
"""The center() function returns a string padded with specified fill-character. It doesn't modify
the original string and default filler is a space
SYNTAX : string.center(width, fillchar)
where 1) width - length of the string with padded character
      2) fillchar (optional) - padding character"""

string = input("Enter the string : ")
print(f'String : {string.center(50, )}')

