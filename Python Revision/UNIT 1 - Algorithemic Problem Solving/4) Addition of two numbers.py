# Addition of two numbers

# Get the integer value from the user namely "first_number", "second_number"
first_number = int(input("Enter the first number : "))
second_number = int(input("Enter the second number : "))

addition_value = first_number + second_number # Calculate the addition of two numbers

print(f"{first_number} + {second_number} = {addition_value}") # Display the addition value
